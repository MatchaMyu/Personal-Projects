void irq_handler(regs_t* r)
{
    uint32_t irq = r->int_no - 32;

    if (irq == 1) {
        keyboard_handler();
    }

    if (interrupt_handlers[r->int_no] != 0)
    {
        isr_t handler = interrupt_handlers[r->int_no];
        handler(r);
    }

    pic_send_eoi((unsigned char)irq);
}
